/** Canonical seed: 97 people. Role "—" / dept "—" preserved for gaps. */
function emailFromName(name) {
  const s = String(name).replace(/,/g, " ").replace(/\s+/g, " ").trim();
  const parts = s.split(" ").filter(Boolean);
  if (parts.length === 0) return "person@company.com";
  const first = parts[0].toLowerCase().replace(/[^a-z0-9]/g, "");
  const last = parts[parts.length - 1].toLowerCase().replace(/[^a-z0-9]/g, "");
  if (parts.length === 1) return `${first}@company.com`;
  return `${first}.${last}@company.com`;
}

function personFromRow(id, name, role, department) {
  const r = role === "—" ? "—" : role;
  const d = department === "—" ? "" : department;
  return {
    id,
    name,
    role: r,
    department: d,
    access: "—",
    tags: [],
    type: "Employee",
    costRate: "0",
    billRate: "0",
    startDate: "2026-01-01",
    endDate: "",
    workType: "Full-time",
    notes: "",
    holidays: "None",
    email: emailFromName(name),
    archived: false,
  };
}

